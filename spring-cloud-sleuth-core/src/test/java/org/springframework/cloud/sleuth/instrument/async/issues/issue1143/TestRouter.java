package org.springframework.cloud.sleuth.instrument.async.issues.issue1143;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.reactive.function.server.RouterFunction;
import org.springframework.web.reactive.function.server.ServerResponse;

import static org.springframework.http.MediaType.APPLICATION_JSON;
import static org.springframework.web.reactive.function.server.RequestPredicates.POST;
import static org.springframework.web.reactive.function.server.RequestPredicates.accept;
import static org.springframework.web.reactive.function.server.RouterFunctions.route;

@Configuration
class TestRouter {

	private static final Logger log = LoggerFactory.getLogger(TestRouter.class);

	@Bean
	public RouterFunction<ServerResponse> testRoutes(TestHandler handler) {
		log.info("FOO 1");
		return route(POST("/test").and(accept(APPLICATION_JSON)), handler::save);
	}

}
